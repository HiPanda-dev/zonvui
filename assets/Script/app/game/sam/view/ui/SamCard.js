var TLMNCard = require('TLMNCard');
var SamCard = cc.Class({
    extends: TLMNCard,

    initComponent: function (componentId, container) {
        TLMNCard.prototype.initComponent.call(this, componentId, container);
    },

    applyLayout: function () {
        TLMNCard.prototype.applyLayout.call(this);
    },

    initialize: function () {
        TLMNCard.prototype.initialize.call(this);
    },

    //////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////override////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////


    //////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////

});

SamCard.create = function (componentId, container) {
    var component = new SamCard();
    component.initComponent(componentId, container);
    return component;
};

module.exports = SamCard;